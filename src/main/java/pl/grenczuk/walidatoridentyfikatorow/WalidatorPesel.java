    /*
    * Metoda zbiera wynik operacji weryfikacji numeru Pesel,
    * w tym rozbite dane cząstkowe i dokonuje ich wstawienia do tabeli
    */
    private void saveText() {
        String textField1Val = PESELNumber.getText();
        String textField2Val = WynikWalidacji.getText();
        String textField3Val = Plec.getText();
        String textField4Val = DzienUrodzenia.getText();
        String textField5Val = MiesiacUrodzenia.getText();
        String textField6Val = RokUrodzenia.getText();
        
        addRow(textField1Val,textField2Val,textField3Val,textField4Val,textField5Val,textField6Val);
    }
    // WŁASNA METODA
    /*
    * Metodat wstawia wynik walidacji do obiektu table -> jTable1
    */
    public void addRow(String str1, String str2, String str3, String str4, String str5, String str6){
        DefaultTableModel tabelModel = (DefaultTableModel) jTable1.getModel();
        tabelModel.addRow(new Object[]{str1, str2, str3, str4, str5, str6});
     }
    
    public void impro(){
               jLabel12.setText(autor);
               jLabel13.setText(rok);
               jLabel14.setText(wersja);
    }
