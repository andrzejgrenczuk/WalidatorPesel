    /*
    * Metoda oblicza ilość wprowadzonych wierszy
    */
    private void LiczWiersze(){
           LacznieSprawdzen.setText(Integer.toString(jTable1.getRowCount()));
    }
    
    private void WalidujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WalidujActionPerformed
        try {
            PeselValidator pv  = new PeselValidator(PESELNumber.getText());
            WynikWalidacji.setText(Boolean.toString(pv.isValid()));
            Plec.setText(pv.getSex());
            DzienUrodzenia.setText(Integer.toString(pv.getBirthDay()));
            MiesiacUrodzenia.setText(Integer.toString(pv.getBirthMonth()));
            RokUrodzenia.setText(Integer.toString(pv.getBirthYear()));
           saveText();
           LiczWiersze();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_WalidujActionPerformed

